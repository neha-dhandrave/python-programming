# Step 1: Create two strings
str1 = "Python"
str2 = "Programming"

# Step 2: Concatenate the strings with a space
full_string = str1 + " " + str2

# Step 3: Print the concatenated string
print("Concatenated String:", full_string)

# Step 4: Access and print a substring
# Let's say we want to extract "thon Pro"
# Index of 't' is 2, index after 'o' in "Pro" is 10
substring = full_string[2:10]

# Step 5: Print the substring
print("Substring from index 2 to 9:", substring)
